#include <stdio.h>
#include "texture_cache.h"

texture_id_t ids[4000];

int main() {
    SDL_Window* window;
    SDL_Renderer* renderer;

    if (0 != SDL_CreateWindowAndRenderer(1024, 600, 0, &window, &renderer)) {
        printf("failed to create window\n");
        exit(EXIT_FAILURE);
    }
    for(int ix=0; ix<sizeof(pngs)/sizeof(pngs[0]); ++ix) {
//        ids[ix] = tcache_put_texture(pngs[ix], (SDL_Texture *)(pngs+ix), true);
        ids[ix] = tcache_load_media(pngs[ix], renderer, true);
    }
    tcache_dump();
    for(int ix=0; ix<sizeof(pngs)/sizeof(pngs[0]); ++ix) {
        texture_id_t texture_id;
        const SDL_Texture *texture1, *texture2;
        texture1 = tcache_quick_get_texture(ids[ix]);
        texture2 = tcache_get_texture(pngs[ix], &texture_id);
        if (texture1 != texture2) {
            printf("%d) texture mismatch %p %p, for %s\n", ix, texture1, texture2, pngs[ix]);
            exit(EXIT_FAILURE);
        }
        if (texture1 == NULL) {
            printf("%d) texture is %p for %s\n", ix, texture1, pngs[ix]);
            exit(EXIT_FAILURE);
        }
//        if (texture1 != (SDL_Texture *)(pngs+ix)) {
//            printf("%d) texture expected value mismatch %p %p, for %s\n", ix, texture1, (SDL_Texture *)(pngs+ix), pngs[ix]);
//            exit(EXIT_FAILURE);
//        }
        if (texture_id != ids[ix]) {
            printf("%d) texture ids mismatch %d %d, for %s\n", ix, ids[ix], texture_id, pngs[ix]);
            exit(EXIT_FAILURE);
        }
    }
    printf("\n");
    int ejected_count = 0;
    while(tcache_eject_lru()) {
        ++ejected_count;
    }
    printf("ejected %d\n", ejected_count);
    printf("\nOK\n");
}

